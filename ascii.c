// ==================== DISPLAY FUNCTION ====================

void show_ascii() {
    clear_screen();
    
    // Pseudo-random screen selector based on memory state
    static int selector = 0;
    selector = (selector + 7) % 6;  // Now we have 6 screens
    
    char** screen;
    int lines;
    
    // Select which horror screen to display
    switch(selector) {
        case 0:
            screen = horror_screen_1;
            lines = 34;
            break;
        case 1:
            screen = horror_screen_2;
            lines = 13;
            break;
        case 2:
            screen = horror_screen_3;
            lines = 15;
            break;
        case 3:
            screen = horror_screen_4;
            lines = 15;
            break;
        case 4:
            screen = horror_screen_5;
            lines = 12;
            break;
        default:
            screen = horror_screen_6;
            lines = 17;
    }
    
    // Center the art vertically
    int start_row = (VGA_HEIGHT - lines) / 2;
    if (start_row < 0) start_row = 0;
    
    // Display with alternating colors for creepy effect
    for (int i = 0; i < lines && (start_row + i) < VGA_HEIGHT - 2; i++) {
        unsigned char color;
        
        // Alternate between different shades of red/gray for unease
        if (i % 3 == 0) {
            color = RED_ON_BLACK;
        } else if (i % 3 == 1) {
            color = MAKE_COLOR(COLOR_LIGHT_RED, COLOR_BLACK);
        } else {
            color = MAKE_COLOR(COLOR_DARK_GRAY, COLOR_BLACK);
        }
        
        print(screen[i], start_row + i, color);
    }
}
